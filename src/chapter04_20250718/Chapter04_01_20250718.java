package chapter04_20250718;

public class Chapter04_01_20250718 {

	public static void main(String[] args) {
		//점수가 70점 이상이면 합격
		int score = 65;
		
		if (score>=70) {
			System.out.println("합격");		
		}
		System.out.println("축하합니다");
		//점수가 70점 이상이면 합격, 70점 미만이면 불합격
		if (score>=70) {
			System.out.println("합격");		
		} else {
			System.out.println("불합격");
		}
		
	}

}
